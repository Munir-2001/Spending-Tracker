"use client";

import { motion } from "framer-motion";
import type { ReactNode } from "react";

/**
 * Lightweight entrance animation. One orchestrated, staggered page load reads
 * as far more considered than scattered micro-interactions.
 */
export function Reveal({
  children,
  delay = 0,
  className,
}: {
  children: ReactNode;
  delay?: number;
  className?: string;
}) {
  return (
    <motion.div
      className={className}
      initial={{ opacity: 0, y: 10 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.55, delay, ease: [0.16, 1, 0.3, 1] }}
    >
      {children}
    </motion.div>
  );
}
